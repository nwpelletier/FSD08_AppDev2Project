// Putting JS script here - for Modal validation (edit user info)
// This script comes from Bootstrap site for validating modal forms
// Asp-validation was not working properly with modal popup so I opted not to use it
(function () {
    'use strict'
    var forms = document.querySelectorAll('.needs-validation')
    Array.prototype.slice.call(forms)
        .forEach(function (form) {
            form.addEventListener('submit', function (event) {
                if (!form.checkValidity()) {
                    event.preventDefault()
                    event.stopPropagation()
                }

                form.classList.add('was-validated')
            }, false)
        })
})()